package edu.femxa.simulador;

import java.util.Scanner;

public class PedirNumero {
	
	
	public int pedirNumero ()
	{
		int pedirNumero = 0;
		Scanner scanner = null;
		scanner = new Scanner(System.in); 
		pedirNumero = scanner.nextInt();
		return pedirNumero;
	}
	
}
	
	
		
	
		
	


